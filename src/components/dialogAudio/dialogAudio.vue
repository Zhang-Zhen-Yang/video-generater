export default {
  name: 'dialog-audio',
  components: { },
  data () {
    return {
      accept: ['.mp3','.wav'].join(',')
    }
  },
  computed: {
    modal(){
      return this.$store.state.dialogAudio;
    },
    list() {
      return this.modal.list;
    },
    audioFrom() {
      return this.modal.audioFrom;
    },
    audioData() {
      return this.modal.audioData;
    },
    selectedAudioID() {
      return this.modal.selectedAudioID;
    }

  },
  methods: {
    dismiss(){
      this.modal.show = false;
    },
    confirm() {
      alert('yes');
    },
    change({file}) {
      // let file = file;
      this.$store.state.audio = file;
      let fileReader = new FileReader();
      fileReader.onload = function(){
        console.warn(fileReader.result);
      }
      fileReader.onerror = function(e){
        console.warn(e);
      }
      fileReader.readAsBinaryString(file);
      // console.log(file);
    },
    fetchAutio() {
      this.$store.state.dialogAudio.audioFrom = 'net';
      this.$store.dispatch('fetchAudio');
    },
    //选择音乐
    selectAudio(item) {
      // alert(item.url);
      this.$store.state.dialogAudio.audioFrom = 'net';
      this.$store.dispatch('fetchAudioNew', {item});
      // this.dismiss();
    },
    // 设为无声
    setMute(){
      this.$store.state.dialogAudio.audioFrom = 'net';
      this.$store.state.dialogAudio.selectedAudioID = null;
      this.dismiss();
    },
    audioChange({file}) {
      let fileReader = new FileReader();
      fileReader.onload = ()=>{
        // alert('dddd');
        let data = fileReader.result.split(',');
        this.$store.state.dialogAudio.audioData = data[1];
        this.$store.state.dialogAudio.audioType = data[0];
        this.$store.state.dialogAudio.audioFrom = 'local';
        // console.warn(fileReader.result);
        this.dismiss();
      }
      fileReader.onerror = function(e){
        console.warn(e);
      }
      fileReader.readAsDataURL(file);
    }


  },
  created() {
    
  }
}
</script>

